					<h3>Wrapping it up</h3>
					<p>Keyboard shortcuts are used by many, so why not keep the tradition going? (Clear isn’t an outcast because it uses gestures, it’s just a different kind of app.) With Mochi, you can do just that. If you navigate your Mac with many a CMD + Space and open Alfred for all the basic actions, you’ll enjoy creating tasks in this app. All it takes to make a new note (the bubble titles you see to the left) is CMD + N. Type a title and press tab to begin making your list of tasks.</p>
